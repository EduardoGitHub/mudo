<h5><u>Recomenda&ccedil;&otilde;es de uso:</u></h5><BR>
&nbsp;&nbsp;&nbsp;&bull;	Toda superf&iacute;cie deve prover uma ades&atilde;o adequada &agrave; pel&iacute;cula auto-adesiva. Superf&iacute;cies de baixa energia superficial n&atilde;o s&atilde;o recomendadas. Quanto ao acabamento, o MMC deve ser aplicado em superf&iacute;cies lisas, n&atilde;o apresentando irregularidades nem porosidade excessiva, livres de qualquer tipo de sujeira, como gorduras, poeiras, graxas ou qualquer outra coisa que dificulte a aplica&ccedil;&atilde;o de um adesivo. <BR>
&nbsp;&nbsp;&nbsp;&bull;	Aplica-se o MMC em paredes que j&aacute; est&atilde;o bem secas ap&oacute;s serem pintadas, de prefer&ecirc;ncia com tinta brilhante ou acetinada ou esmalte sint&eacute;tico. N&atilde;o adere em parede com textura ou quaisquer tipo de tecido.<BR>
